import json
import random
import os
from pathlib import Path

# Try to import transformers to verify token lengths (CRITICAL for EAP-IG)
try:
    from transformers import AutoTokenizer
    TOKENIZER_ID = "google/functiongemma-270m-it" # Gemma tokenizer
    tokenizer = AutoTokenizer.from_pretrained(TOKENIZER_ID)
    print("Tokenizer loaded successfully for precise token alignment checks.")
except ImportError:
    tokenizer = None
    print("WARNING: 'transformers' not installed. Token lengths won't be strictly verified. Install with `pip install transformers`.")

# -------------------------------------------------------------------------
# 1. DEFINE TOOLS WITH UNIQUE FIRST TOKENS
# We rename tools so the FIRST token generated by the model is entirely unique.
# e.g., 'measure', 'evaluate', 'check', 'track'
# -------------------------------------------------------------------------

TOOL_A = {
    "type": "function",
    "function": {
        "name": "measure_temperature", # Target token: "measure"
        "description": "Gets the current temperature for a given location.",
        "parameters": {
            "type": "object",
            "properties": {
                "location": {"type": "string", "description": "The city name, e.g. London"},
            },
            "required": ["location"],
        },
    }
}

TOOL_B = {
    "type": "function",
    "function": {
        "name": "evaluate_math", # Target token: "evaluate"
        "description": "Evaluates a mathematical expression.",
        "parameters": {
            "type": "object",
            "properties": {
                "expression": {"type": "string", "description": "The math expression, e.g. '2 + 2'"},
            },
            "required": ["expression"],
        },
    }
}

TOOL_C = {
    "type": "function",
    "function": {
        "name": "check_stock_price", # Target token: "check"
        "description": "Gets the current stock price for a given ticker.",
        "parameters": {
            "type": "object",
            "properties": {
                "ticker": {"type": "string", "description": "The stock ticker symbol, e.g. AAPL"},
            },
            "required": ["ticker"],
        },
    }
}

TOOL_D = {
    "type": "function",
    "function": {
        "name": "track_flight_status", # Target token: "track"
        "description": "Gets the current status of a flight.",
        "parameters": {
            "type": "object",
            "properties": {
                "flight_number": {"type": "string", "description": "The flight number, e.g. BA123"},
            },
            "required":["flight_number"],
        },
    }
}

# Map tool names to their actual expected FIRST generation token for the metric
TARGET_TOKENS = {
    "measure_temperature": "measure",
    "evaluate_math": "evaluate",
    "check_stock_price": "check",
    "track_flight_status": "track"
}

# -------------------------------------------------------------------------
# 2. STRICT MINIMAL PAIR GENERATION
# To do EAP-IG, the prompt structure must be identical. We only change 1 noun.
# -------------------------------------------------------------------------

TEMPLATES =[
    "Could you check the {domain} for {entity}?",
    "I need you to process the {domain} of {entity}.",
    "Please provide the {domain} data for {entity}.",
    "What is the current {domain} for {entity}?"
]

# Distinct 1-token words that strongly bias towards a specific tool
DOMAINS = {
    "measure_temperature":["weather", "climate", "temp"],
    "evaluate_math": ["numbers", "algebra", "math"],
    "check_stock_price": ["finance", "stocks", "market"],
    "track_flight_status":["flights", "transit", "airway"]
}

# Deliberately ambiguous entities. 
# "Delta" is an airline (flights) and a math term (evaluate).
# "Apple" is a company (stocks) and a generic noun.
# This forces the model's tool selection circuit to rely heavily on the {domain} token!
ENTITIES =[
    "Apple", "Delta", "Amazon", "Boston", "London", 
    "Target", "United", "Matrix", "Ford", "Paris"
]

def generate_tool_selection_dataset(num_samples=1000, output_dir=None, seed=42):
    if output_dir is None:
        output_dir = Path(__file__).resolve().parent.parent.parent / "data" / "tool_selection"

    random.seed(seed)
    os.makedirs(output_dir, exist_ok=True)

    dataset =[]
    tools_list = list(TARGET_TOKENS.keys())
    
    attempts = 0
    valid_samples = 0

    while valid_samples < num_samples and attempts < num_samples * 5:
        attempts += 1
        
        # 1. Pick two distinct tools
        clean_tool, corrupted_tool = random.sample(tools_list, 2)
        
        # 2. Pick domain nouns and entity
        clean_domain = random.choice(DOMAINS[clean_tool])
        corrupted_domain = random.choice(DOMAINS[corrupted_tool])
        entity = random.choice(ENTITIES)
        template = random.choice(TEMPLATES)

        # 3. Construct minimal pair user queries
        clean_query = template.format(domain=clean_domain, entity=entity)
        corrupted_query = template.format(domain=corrupted_domain, entity=entity)

        # 4. Token Alignment Check (CRITICAL FOR EAP)
        if tokenizer:
            clean_tokens = tokenizer.encode(clean_query, add_special_tokens=False)
            corr_tokens = tokenizer.encode(corrupted_query, add_special_tokens=False)
            
            # If lengths differ, EAP positional patching will fail. Skip this pair.
            if len(clean_tokens) != len(corr_tokens):
                continue

        # 5. Format using FunctionGemma's required 'developer' role
        clean_messages =[
            {"role": "developer", "content": "You are a model that can do function calling with the following functions"},
            {"role": "user", "content": clean_query}
        ]

        corrupted_messages =[
            {"role": "developer", "content": "You are a model that can do function calling with the following functions"},
            {"role": "user", "content": corrupted_query}
        ]

        dataset.append({
            "clean_messages": clean_messages,
            "corrupted_messages": corrupted_messages,
            "clean_target_tool": clean_tool,
            "corrupted_target_tool": corrupted_tool,
            "clean_target_token": TARGET_TOKENS[clean_tool],
            "corrupted_target_token": TARGET_TOKENS[corrupted_tool],
            "tools_schema": [TOOL_A, TOOL_B, TOOL_C, TOOL_D],
            # Helper for the EAP-IG loop to force generation
            "required_prefix": "<start_function_call>call:" 
        })
        
        valid_samples += 1

    file_path = os.path.join(output_dir, "eap_ig_tool_selection_dataset.json")

    with open(file_path, "w", encoding="utf-8") as f:
        json.dump(dataset, f, indent=2)

    print(f"Generated {len(dataset)} perfectly aligned EAP minimal pairs at {file_path}")
    if len(dataset) < num_samples:
        print("Note: Generated fewer samples than requested due to strict tokenizer alignment filtering.")
        
    return file_path

if __name__ == "__main__":
    generate_tool_selection_dataset()